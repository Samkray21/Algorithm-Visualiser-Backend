package com.algorithm.Algorithm.SpanningTreeAlgorithms.Common;

public class NumToStringConverter {

    public String convertNumToString(int edge){
        String newString = "";
        switch (edge){
            case 0:
                newString = "A";
                break;
            case 1:
                newString = "B";
                break;
            case 2:
                newString = "C";
                break;
            case 3:
                newString = "D";
                break;
            case 4:
                newString = "E";
                break;
            case 5:
                newString = "F";
                break;
            case 6:
                newString = "G";
                break;
            case 7:
                newString = "H";
                break;
            case 8:
                newString = "I";
                break;
            case 9:
                newString = "J";
                break;
        }
        return newString;
    }

}
